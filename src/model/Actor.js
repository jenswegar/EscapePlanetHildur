
/**
 * Represents an NPC or other object that is dynamic and might interact 
 * with the player.
 */
class Actor {

    /**
     * The current location of the Actor
     */
    inRoom = 0;

    /**
     * An array of strings that are shown when the player interacts with this character
     * 
     * The strings are used from the first to the last in the array
     * 
     */
    greetings = [];


}